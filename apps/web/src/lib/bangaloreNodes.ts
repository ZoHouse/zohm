// Helper function to check if coordinates are within Bangalore bbox
export function isWithinBangalore(lat: number, lng: number): boolean {
    return (
        lat >= BANGALORE_BBOX.south &&
        lat <= BANGALORE_BBOX.north &&
        lng >= BANGALORE_BBOX.west &&
        lng <= BANGALORE_BBOX.east
    );
}

// Get nodes by category
export function getBangaloreNodesByCategory(category: PoiCategory): DestinationNode[] {
    return ALL_BANGALORE_NODES.filter(node => node.poi_category === category);
}

